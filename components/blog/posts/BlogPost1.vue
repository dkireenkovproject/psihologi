привязанностью. Люди, которые в детстве не чувствовали себя в безопасности
или не получали достаточной поддержки, могут бояться близких отношений, так
как они ассоциируются с болью и потерей. В результате такие люди могут
избегать глубоких эмоциональных связей, даже если в глубине души они их
желают.</p>
  </li>
  <li class="blog__item">
<p class="blog__descriptio-2"><span class="blog__span">2. Проблемы с доверием</span><br>
  Дети, которым в детстве постоянно приходилось разочаровываться в людях,
могут во взрослой жизни испытывать трудности с доверием к партнерам. Они
могут постоянно сомневаться в искренности чувств партнера, искать признаки
обмана или предательства и бояться открыться полностью.</p>
  </li>
  <li class="blog__item">
<p class="blog__descriptio-2"><span class="blog__span">3. Зависимость или контроль</span><br>
  Некоторые люди, пережившие детские травмы, могут стать слишком
зависимыми от партнера или, наоборот, пытаться контролировать его. В первом
случае человек может бояться потерять отношения и цепляться за них, даже
если они не приносят удовлетворения. Во втором — партнер может проявлять
чрезмерный контроль, боясь снова быть уязвимым или преданным.</p>
  </li>
  <li class="blog__item">
<p class="blog__descriptio-2"><span class="blog__span">4. Самосаботаж в отношениях</span><br>
  Когда человек сталкивается с внутренними конфликтами, связанными с
детскими травмами, он может неосознанно разрушать свои отношения.
Например, человек может провоцировать партнера на конфликты, чтобы
подтвердить свои негативные ожидания (“все равно меня бросят”). Самосаботаж
может проявляться в виде измен, отказа от серьезных шагов в отношениях или
намеренного разрушения эмоциональной связи.</p>
  </li>
</ul>
