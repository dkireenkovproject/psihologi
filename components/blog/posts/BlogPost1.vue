отношения — источник боли. Это может сформировать в человеке защитный
механизм, который проявляется в недоверии к партнерам, в чувстве, что любовь
обязательно сопровождается болью и контролем.</p>
<p class="blog__descriptio-2"><span class="blog__span">Последствия в романтических отношениях:</span><br>
