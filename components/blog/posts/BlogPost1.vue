ревнивыми, чтобы избежать потенциальной угрозы, либо, напротив, могут
выбирать токсичных партнеров, которые продолжают цикл насилия. Им может
быть сложно распознать здоровые границы и поддерживать отношения без
постоянного ощущения тревоги.</p> 
